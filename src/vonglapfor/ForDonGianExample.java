package vonglapfor;

public class ForDonGianExample {
    public static void main(String[] args) {
        for (int i =10; i>1; i=i-2){
            System.out.println(i);
        }

        for (int i=1; i<10;i++){
            System.out.println(i);
        }

        // cách dùng khi nào dùng for đơn giản : khi caác định được ị trí điểm đầu và kết thúc
    }
}
